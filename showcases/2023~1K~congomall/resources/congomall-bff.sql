CREATE TABLE `donation`
(
    `id`          bigint(20) NOT NULL AUTO_INCREMENT COMMENT 'ID',
    `nick_name`   varchar(256) DEFAULT NULL COMMENT '昵称',
    `pay_type`    tinyint(1) DEFAULT NULL COMMENT '支付方式 0：支付宝',
    `money`       int(3) DEFAULT NULL COMMENT '捐赠金额',
    `info`        text COMMENT '留言',
    `time`        datetime     DEFAULT NULL COMMENT '捐赠时间',
    `create_time` datetime     DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `update_time` datetime     DEFAULT CURRENT_TIMESTAMP COMMENT '修改时间',
    `del_flag`    tinyint(1) DEFAULT '0' COMMENT '删除标记',
    PRIMARY KEY (`id`),
    UNIQUE KEY `id` (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=12 DEFAULT CHARSET=utf8mb4 COMMENT='捐赠表';

CREATE TABLE `navigation`
(
    `id`          int(20) NOT NULL AUTO_INCREMENT COMMENT 'ID',
    `name`        varchar(256) COLLATE utf8mb4_bin  DEFAULT NULL COMMENT '名称',
    `url`         varchar(2048) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '跳转地址',
    `sort`        int(3) DEFAULT NULL COMMENT '排序',
    `create_time` datetime                          DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `update_time` datetime                          DEFAULT CURRENT_TIMESTAMP COMMENT '修改时间',
    `del_flag`    tinyint(1) DEFAULT '0' COMMENT '删除标记',
    PRIMARY KEY (`id`),
    UNIQUE KEY `id` (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=6 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_bin COMMENT='导航表';

CREATE TABLE `panel`
(
    `id`          bigint(20) NOT NULL AUTO_INCREMENT COMMENT 'ID',
    `name`        varchar(256) DEFAULT NULL COMMENT '名称',
    `position`    int(3) DEFAULT NULL COMMENT '位置',
    `limit_num`   int(3) DEFAULT NULL COMMENT '限制数量',
    `sort_order`  int(3) DEFAULT NULL COMMENT '排序',
    `status`      int(3) DEFAULT NULL COMMENT '状态',
    `type`        int(3) DEFAULT NULL COMMENT '类型',
    `remark`      varchar(512) DEFAULT NULL COMMENT '备注',
    `create_time` datetime     DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `update_time` datetime     DEFAULT CURRENT_TIMESTAMP COMMENT '修改时间',
    `del_flag`    tinyint(1) DEFAULT '0' COMMENT '删除标记',
    PRIMARY KEY (`id`),
    UNIQUE KEY `id` (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=13 DEFAULT CHARSET=utf8mb4 COMMENT='板块表';

CREATE TABLE `panel_product_relation`
(
    `id`          bigint(20) NOT NULL AUTO_INCREMENT COMMENT 'ID',
    `panel_id`    bigint(20) DEFAULT NULL COMMENT '板块ID',
    `product_id`  bigint(20) DEFAULT NULL COMMENT '商品ID',
    `big_pic`     varchar(256) DEFAULT NULL COMMENT '大图',
    `pic`         varchar(512) DEFAULT NULL COMMENT '图集',
    `sort`        int(3) DEFAULT NULL COMMENT '排序',
    `create_time` datetime     DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `update_time` datetime     DEFAULT CURRENT_TIMESTAMP COMMENT '修改时间',
    `del_flag`    tinyint(1) DEFAULT '0' COMMENT '删除标记',
    PRIMARY KEY (`id`),
    UNIQUE KEY `id` (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=39 DEFAULT CHARSET=utf8mb4 COMMENT='板块商品关联表';

